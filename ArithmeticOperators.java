class ArithmeticOperators {
  // Arithmetic operators are used to perform arithmetic operations on variables and data
  public static void main(String[] args) {
    int a = 12, b = 5;
    System.out.println("a + b = " + (a + b));
    System.out.println("a - b = " + (a - b));
    System.out.println("a * b = " + (a * b));
    System.out.println("a / b = " + (a / b));
    System.out.println("a % b = " + (a % b));
  }
}